// 텍스트 내용을 HTML로 변환
function contentToHtml(text) {
  const lines = text.split('\n')
  const result = []

  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed) {
      result.push('')
      continue
    }

    // HTML 이스케이프 (& 만 처리 - 나머지는 UTF-8로 직접 저장)
    const esc = trimmed
      .replace(/&amp;/g, '&').replace(/&quot;/g, '"').replace(/&apos;/g, "'")
      .replace(/&/g, '&amp;')

    // 섹션 헤더 (이모지로 시작하며 비교적 짧은 라인)
    const isHeader = /^[💡🚨✅❌🔥⚠️🛡️🎯📋📅💬😱😔🌟💰💍📱🛍️😊💪🏠✈️🎊🔍🕳️😤🏡🔴⚡⏰😰🗓️🏛️📍💥💴]/.test(trimmed) && trimmed.length < 55

    if (isHeader) {
      result.push(`<p><strong>${esc}</strong></p>`)
    } else if (/^→/.test(trimmed) || /^[①②③④⑤]/.test(trimmed)) {
      result.push(`<p style="margin-left:14px">${esc}</p>`)
    } else {
      result.push(`<p>${esc}</p>`)
    }
  }

  // 빈 줄 연속 제거
  return result.filter((l, i) => !(l === '' && (result[i-1] === '' || i === 0 || i === result.length - 1))).join('\n')
}

async function addColumnViaApi() {
  // Supabase SQL API (service_role 없이 anon으로 시도)
  const sql = `ALTER TABLE board_posts ADD COLUMN IF NOT EXISTS category TEXT DEFAULT '꿀팁 정보';`
  try {
    const res = await fetch(`${SUPABASE_URL}/rest/v1/rpc/exec`, {
      method: 'POST',
      headers: { 'apikey': SUPABASE_KEY, 'Authorization': `Bearer ${SUPABASE_KEY}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({ sql })
    })
    return res.ok
  } catch { return false }
}

