// HTML escape 유틸
function escHtml(str) {
  return str
    .replace(/&amp;/g, '&')
    .replace(/&quot;/g, '"')
    .replace(/&apos;/g, "'")
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

// 텍스트 블록을 HTML로 변환
function textToHtml(text) {
  const lines = text.split('\n')
  const result = []
  let inBlock = false

  for (let line of lines) {
    const trimmed = line.trim()
    if (!trimmed) {
      if (inBlock) { result.push(''); inBlock = false }
      continue
    }

    const esc = escHtml(trimmed)

    // 섹션 헤더처럼 보이는 라인 (이모지로 시작하며 짧음)
    const isHeader = /^[💡🚨✅❌🔥⚠️🛡️🎯📋📅💬😱😔🌟💰💍📱🛍️😊💪🏠✈️🎊🔍]/.test(trimmed) && trimmed.length < 60
    const isListItem = /^(→|✅|❌|\d+\.|[①②③]|방법|방식|단계|순서|패턴|갈등|이유|상황|기준|시기|유형|구분|항목|장소|탭)/.test(trimmed)

    if (isHeader) {
      result.push(`<p><strong>${esc}</strong></p>`)
      inBlock = false
    } else if (isListItem || /^[→]/.test(trimmed)) {
      result.push(`<p style="margin-left:12px">${esc}</p>`)
      inBlock = false
    } else {
      result.push(`<p>${esc}</p>`)
      inBlock = true
    }
  }

  return result.filter((l, i) => !(l === '' && result[i-1] === '')).join('\n')
}

